package pbouda.jeffrey.profile.guardian.guard.alloc;

import pbouda.jeffrey.profile.common.analysis.AnalysisResult;
import pbouda.jeffrey.profile.guardian.Formatter;
import pbouda.jeffrey.profile.guardian.guard.TraversableGuard;
import pbouda.jeffrey.profile.guardian.matcher.FrameMatchers;
import pbouda.jeffrey.profile.guardian.preconditions.Preconditions;
import pbouda.jeffrey.profile.guardian.traverse.MatchingType;
import pbouda.jeffrey.profile.guardian.traverse.ResultType;
import pbouda.jeffrey.profile.guardian.traverse.TargetFrameType;

public class Log4jAllocGuard extends TraversableGuard {

    public Log4jAllocGuard(ProfileInfo profileInfo, double threshold) {
        super("Log4j Allocation Overhead",
                profileInfo,
                threshold,
                FrameMatchers.prefix("org.apache.logging.log4j."),
                Category.APPLICATION,
                TargetFrameType.JAVA,
                MatchingType.FULL_MATCH,
                ResultType.WEIGHT);
    }

    @Override
    protected String summary() {
        Result result = getResult();

        String direction = result.severity() == AnalysisResult.Severity.OK ? "lower" : "higher";
        return "The ratio between total allocations (" + result.totalValue() + ") and " +
                "allocations caused by Log4j logging (" + result.observedValue() + ") " +
                "is " + direction + " than the threshold (" +
                Formatter.formatRatio(result.ratioResult()) + " / " + result.threshold() + ").";
    }

    @Override
    protected String explanation() {
        return """
                Log4j2 logging operations allocate log event objects, formatted message strings, and
                various internal buffers. Under heavy load, these allocations can create significant
                GC pressure, especially with synchronous appenders and complex log message formatting.
                """;
    }

    @Override
    protected String solution() {
        Result result = getResult();
        if (result.severity() == AnalysisResult.Severity.OK) {
            return null;
        } else {
            return """
                    <ul>
                        <li>Use parameterized messages to avoid unnecessary string formatting
                        <li>Enable garbage-free logging mode in Log4j2 configuration
                        <li>Consider using asynchronous appenders to reduce allocation pressure on application threads
                        <li>Review logging levels and reduce unnecessary log statements in hot paths
                    </ul>
                    """;
        }
    }

    @Override
    public Preconditions preconditions() {
        return Preconditions.builder().build();
    }
}
