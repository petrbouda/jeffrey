package pbouda.jeffrey.profile.guardian.guard.blocking;

import pbouda.jeffrey.profile.common.analysis.AnalysisResult;
import pbouda.jeffrey.profile.guardian.Formatter;
import pbouda.jeffrey.profile.guardian.guard.TraversableGuard;
import pbouda.jeffrey.profile.guardian.matcher.FrameMatchers;
import pbouda.jeffrey.profile.guardian.preconditions.Preconditions;
import pbouda.jeffrey.profile.guardian.traverse.MatchingType;
import pbouda.jeffrey.profile.guardian.traverse.ResultType;
import pbouda.jeffrey.profile.guardian.traverse.TargetFrameType;

public class HttpClientBlockingGuard extends TraversableGuard {

    public HttpClientBlockingGuard(ProfileInfo profileInfo, double threshold) {
        super("HTTP Client Blocking",
                profileInfo,
                threshold,
                FrameMatchers.prefix("java.net.http.")
                        .or(FrameMatchers.prefix("org.apache.http."))
                        .or(FrameMatchers.prefix("okhttp3.")),
                Category.APPLICATION,
                TargetFrameType.JAVA,
                MatchingType.FULL_MATCH,
                ResultType.WEIGHT);
    }

    @Override
    protected String summary() {
        Result result = getResult();

        String direction = result.severity() == AnalysisResult.Severity.OK ? "lower" : "higher";
        return "The ratio between total blocking time (" + result.totalValue() + ") and " +
                "time spent waiting for HTTP responses (" + result.observedValue() + ") " +
                "is " + direction + " than the threshold (" +
                Formatter.formatRatio(result.ratioResult()) + " / " + result.threshold() + ").";
    }

    @Override
    protected String explanation() {
        return """
                Threads are blocked waiting for HTTP client operations to complete. This includes waiting
                for connection establishment, TLS handshakes, and response data. High blocking time indicates
                slow downstream services or insufficient connection pooling.
                """;
    }

    @Override
    protected String solution() {
        Result result = getResult();
        if (result.severity() == AnalysisResult.Severity.OK) {
            return null;
        } else {
            return """
                    <ul>
                        <li>Use asynchronous HTTP client APIs to avoid blocking threads
                        <li>Configure connection pooling and keep-alive to reuse connections
                        <li>Set appropriate connection and read timeouts
                        <li>Consider using circuit breakers for unreliable downstream services
                    </ul>
                    """;
        }
    }

    @Override
    public Preconditions preconditions() {
        return Preconditions.builder().build();
    }
}
