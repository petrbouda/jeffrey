package pbouda.jeffrey.profile.guardian.guard.app;

import pbouda.jeffrey.profile.common.analysis.AnalysisResult;
import pbouda.jeffrey.profile.guardian.Formatter;
import pbouda.jeffrey.profile.guardian.guard.TraversableGuard;
import pbouda.jeffrey.profile.guardian.matcher.FrameMatchers;
import pbouda.jeffrey.profile.guardian.preconditions.Preconditions;
import pbouda.jeffrey.profile.guardian.traverse.MatchingType;
import pbouda.jeffrey.profile.guardian.traverse.ResultType;
import pbouda.jeffrey.profile.guardian.traverse.TargetFrameType;

public class JSONProcessingOverheadGuard extends TraversableGuard {

    public JSONProcessingOverheadGuard(ProfileInfo profileInfo, double threshold) {
        super("JSON Processing Overhead",
                profileInfo,
                threshold,
                FrameMatchers.composite(
                        FrameMatchers.prefix("com.fasterxml.jackson."),
                        FrameMatchers.prefix("com.google.gson.")),
                Category.APPLICATION,
                TargetFrameType.JAVA,
                MatchingType.FULL_MATCH,
                ResultType.SAMPLES);
    }

    @Override
    protected String summary() {
        Result result = getResult();

        String direction = result.severity() == AnalysisResult.Severity.OK ? "lower" : "higher";
        return "The ratio between a total number of samples (" + result.totalValue() + ") and " +
                "samples with JSON processing activity (" + result.observedValue() + ") " +
                "is " + direction + " than the threshold (" +
                Formatter.formatRatio(result.ratioResult()) + " / " + result.threshold() + ").";
    }

    @Override
    protected String explanation() {
        return """
                JSON parsing and serialization (Jackson, Gson) can consume significant CPU when processing
                large payloads or when used in high-throughput paths. Object mapping through reflection,
                type resolution, and string processing all contribute to the overhead.
                """;
    }

    @Override
    protected String solution() {
        Result result = getResult();
        if (result.severity() == AnalysisResult.Severity.OK) {
            return null;
        } else {
            return """
                    <ul>
                        <li>Use streaming APIs (JsonParser/JsonGenerator) instead of tree model for large payloads
                        <li>Reuse ObjectMapper instances (they are thread-safe in Jackson)
                        <li>Consider using Jackson afterburner/blackbird module for faster serialization
                        <li>Reduce the size of serialized objects by excluding unnecessary fields
                    </ul>
                    """;
        }
    }

    @Override
    public Preconditions preconditions() {
        return Preconditions.builder().build();
    }
}
