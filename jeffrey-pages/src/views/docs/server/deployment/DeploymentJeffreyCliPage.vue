      <h2 id="init-flow">Init Flow at Container Start</h2>
      <ol>
        <li>The wrapped entrypoint checks <code>JEFFREY_ENABLED</code>. If it's not <code>true</code>, it skips profiling entirely and <code>exec</code>s the original <code>java</code> command.</li>
        <li>It reads <code>JEFFREY_BASE_CONFIG</code> (the path to the HOCON config inside the container).</li>
        <li>It locates the per-arch CLI binary at <code>${JEFFREY_HOME}/libs/current/jeffrey-cli-&lt;arch&gt;</code> (where <code>&lt;arch&gt;</code> is resolved from <code>uname -m</code>).</li>
        <li>It calls <code>jeffrey-cli init</code>, which reads the HOCON config, derives the agent / profiler paths from <code>JEFFREY_HOME</code>, and emits a JVM-arg response file.</li>
        <li>It launches the JVM with <code>java @&lt;response-file&gt; @/app/jib-classpath-file &lt;MainClass&gt;</code>. The response file injects the <code>-javaagent</code>, the <code>-agentpath</code> for async-profiler, the <code>additional-jvm-options</code>, and the per-feature flags (heap-dump, jvm-logging, perf-counters).</li>
      </ol>

