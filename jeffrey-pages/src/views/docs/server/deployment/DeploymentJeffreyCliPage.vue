      <h2 id="capture-blocks">Capture Blocks</h2>
      <p>Each capture feature toggles independently:</p>

      <h3>perf-counters</h3>
      <p>
        Captures JVM perfdata counters (<code>hsperfdata</code>) every cycle and bundles
        them with the recording. Useful for cross-referencing GC / safepoint / classloading
        rates without reading them out of the JFR stream.
      </p>

      <h3>heap-dump</h3>
      <p>
        <code>type = "crash"</code> writes a heap dump only when the JVM crashes (via
        <code>-XX:HeapDumpOnOutOfMemoryError</code> + crash hooks). Set
        <code>type = "always"</code> for opt-in dump-per-cycle, or <code>enabled =
        false</code> to disable entirely.
      </p>

      <h3>jvm-logging</h3>
      <p>
        Wires up <code>-Xlog:</code> with the supplied command. The
        <code>&lt;&lt;JEFFREY_CURRENT_SESSION&gt;&gt;</code> placeholder is expanded by the
        CLI to the per-cycle session directory, so each recording lands in its own folder
        with its own rolling log file.
      </p>

      <h2 id="jvm-options">Additional JVM Options</h2>
      <p>
        <code>additional-jvm-options</code> is appended verbatim to the JVM launch line —
        it's where the testapp pins heap size, GC choice, and any other JVM flags. The
        same placeholder system applies (<code>&lt;&lt;JEFFREY_CURRENT_SESSION&gt;&gt;</code>,
        <code>&lt;&lt;JEFFREY_PROFILER_PATH&gt;&gt;</code>, etc.).
      </p>

      <DocsCallout type="tip">
        Keep <code>additional-jvm-options</code> short — it composes with profiler agent
        flags injected automatically by <code>jeffrey-cli</code>. Use it for heap sizing
        and GC tuning, not for tweaking the profiler (do that on the
        <router-link to="/docs/microscope/profiler-settings">Profiler Settings</router-link>
        page in Microscope, which propagates to every monitored pod via Jeffrey Server's
        gRPC API).
      </DocsCallout>

      <h2 id="omitted-paths">Why Paths Are Omitted</h2>
      <p>
        The conf file deliberately leaves out <code>jeffrey-home</code>,
        <code>profiler-path</code>, <code>agent-path</code>, and <code>cli-path</code>.
        That's because:
      </p>

      <ul>
        <li><code>jeffrey-home</code> is read from the <code>JEFFREY_HOME</code> env var (set by the chart from <code>sharedVolume.mountPath</code>).</li>
        <li><code>profiler-path</code> defaults to <code>${jeffrey-home}/libs/current/libasyncProfiler.so</code>.</li>
        <li><code>agent-path</code> defaults to <code>${jeffrey-home}/libs/current/jeffrey-agent.jar</code>.</li>
        <li><code>cli-path</code> defaults to <code>${jeffrey-home}/libs/current/jeffrey-cli-&lt;arch&gt;</code>.</li>
      </ul>

      <p>
        Each value baked by the JIB extension at image build time matches what
        <code>copy-libs</code> writes at runtime, so the conf file stays small and the
        same template works for every monitored pod regardless of which Jeffrey Server
        version published the bundle.
